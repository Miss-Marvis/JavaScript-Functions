
// Write a function that takes an input which represents  the temperature
// in Celsius and then converts it to Farenheit
function
celsiusToFahrenheit(celsius) {
    var fahrenheit = (  celsius * 9/5) + 32;
    return fahrenheit;
}

function
fahrenheitToCelsius(fahrenheit) {
    var celsius = (fahrenheit * 32) + 9/5;
    return celsius;
}